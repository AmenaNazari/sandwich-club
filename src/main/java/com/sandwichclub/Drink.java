package com.sandwichclub;

public class Drink {
    private String flavor;
    private String DrinkSize;

    public Drink(String flavor, String drinkSize) {
        this.flavor = flavor;
        DrinkSize = drinkSize;
    }

    public String getFlavor() {
        return flavor;
    }

    public String getDrinkSize() {
        return DrinkSize;
    }
}








